import java.util.*;

class Pattern {
    public static void main(String args[]) {
        System.out.println("Pattern acc to User :- \n");

        int n1 = 2;
        int n2 = 1;
        int n3 = 3;
        int n4 = 6;

        // 1st Pattern
        for(int i = 1; i <= n1; i++) {
            System.out.print("* ");
        }
        System.out.println();

        // 2nd Pattern
        for(int i = 1; i <= n2; i++) {
            System.out.print("* ");
        }
        System.out.println();

        // 3rd Pattern
        for(int i = 1; i <= n3; i++) {
            System.out.print("* ");
        }
        System.out.println();

        // 4th Pattern
        for(int i = 1; i <= n4; i++) {
            System.out.print("* ");
        }
        System.out.println();
    }
}